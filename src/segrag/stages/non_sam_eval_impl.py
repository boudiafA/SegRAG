def parse_thresholds(spec: str) -> list[float]:
    if ":" not in spec:
        return [float(spec)]
    start_s, stop_s, step_s = spec.split(":")
    start = float(start_s)
    stop = float(stop_s)
    step = float(step_s)
    if step <= 0:
        raise ValueError("Threshold step must be > 0")
    values = []
    current = start
    while current <= stop + 1e-9:
        values.append(round(current, 10))
        current += step
    return values


def _build_records(lvis_api, img_ids: list[int], image_dir: str) -> list[LVISImageRecord]:
    records = []
    for img_id in tqdm(img_ids, desc="Scanning annotations"):
        img_info = lvis_api.load_imgs([img_id])[0]
        file_name = (img_info.get("file_name", "") or "").strip()
        if not file_name:
            coco_url = (img_info.get("coco_url", "") or "").strip()
            file_name = os.path.basename(coco_url)
        if not file_name:
            continue
        jpg_path = os.path.join(image_dir, file_name)
        if not os.path.isfile(jpg_path):
            continue
        ann_ids = lvis_api.get_ann_ids(img_ids=[img_id])
        anns = lvis_api.load_anns(ann_ids)
        if not anns:
            continue
        cats_in_img = set(ann["category_id"] for ann in anns)
        not_exhaustive = set(img_info.get("not_exhaustive_category_ids", []))
        valid_cats = cats_in_img - not_exhaustive
        if not valid_cats:
            continue
        records.append(LVISImageRecord(img_id, img_info, file_name, jpg_path, anns, valid_cats))
    return records


def _deserialize_point_tuple(payload: dict | None) -> tuple[np.ndarray, np.ndarray, np.ndarray]:
    if not payload:
        return (
            np.empty((0, 2), dtype=np.float32),
            np.empty(0, dtype=np.int32),
            np.empty(0, dtype=np.float32),
        )
    return (
        np.asarray(payload.get("coords", []), dtype=np.float32).reshape(-1, 2),
        np.asarray(payload.get("labels", []), dtype=np.int32),
        np.asarray(payload.get("scores", []), dtype=np.float32),
    )


def _resolve_paths(args):
    defaults = default_dataset_paths(args.dataset_root)
    args.annotation_file = args.annotation_file or defaults.annotation_file
    args.image_dir = args.image_dir or defaults.image_dir
    args.feature_bank_dir = args.feature_bank_dir or defaults.filtered_feature_bank_dir


def _prompt_cache_dir(args, method: str) -> str:
    if method == "relative_similarity":
        config = {
            "name": "relative_similarity",
            "feature_bank_dir": None,
            "filtered_bank_dir": args.feature_bank_dir,
            "peak_threshold": args.relative_threshold,
            "min_peak_distance": args.min_peak_distance,
            "suppression_margin": args.suppression_margin,
            "no_suppression": args.no_suppression,
            "max_references": args.max_references,
        }
    else:
        config = {
            "name": "hybrid",
            "filtered_bank_dir": args.feature_bank_dir,
            "loose_threshold": args.hybrid_threshold,
            "min_peak_distance": args.min_peak_distance,
            "min_component_size": args.min_component_size,
            "max_references": args.max_references,
        }
    return build_prompt_cache_dir(args.dataset_root, method, config)


class PromptPointEvaluator:
    def __init__(self, classes: dict[int, str]):
        self.classes = classes
        self.tp = defaultdict(int)
        self.fp = defaultdict(int)
        self.found = defaultdict(int)
        self.missed = defaultdict(int)
        self.prompts = defaultdict(int)

    def update(self, class_id: int, coords: np.ndarray, gt_instances: list[np.ndarray]):
        instance_hit = [False] * len(gt_instances)
        class_tp = 0
        class_fp = 0
        for point in coords:
            x = int(round(float(point[0])))
            y = int(round(float(point[1])))
            hit_any = False
            for idx, gt_mask in enumerate(gt_instances):
                yy = min(max(y, 0), gt_mask.shape[0] - 1)
                xx = min(max(x, 0), gt_mask.shape[1] - 1)
                if gt_mask[yy, xx] > 0:
                    hit_any = True
                    instance_hit[idx] = True
            if hit_any:
                class_tp += 1
            else:
                class_fp += 1
        self.tp[class_id] += class_tp
        self.fp[class_id] += class_fp
        self.found[class_id] += sum(instance_hit)
        self.missed[class_id] += len(gt_instances) - sum(instance_hit)
        self.prompts[class_id] += len(coords)

    @staticmethod
    def _metrics(tp: int, fp: int, found: int, missed: int, prompts: int) -> dict:
        precision = tp / (tp + fp) if (tp + fp) > 0 else 0.0
        recall = found / (found + missed) if (found + missed) > 0 else 0.0
        f1 = 2 * precision * recall / (precision + recall) if (precision + recall) > 0 else 0.0
        return {
            "PointPrecision": precision,
            "InstanceRecall": recall,
            "F1": f1,
            "point_tp": tp,
            "point_fp": fp,
            "inst_found": found,
            "inst_missed": missed,
            "total_peaks": prompts,
            "total_instances": found + missed,
        }

    def global_metrics(self) -> dict:
        return self._metrics(
            sum(self.tp.values()),
            sum(self.fp.values()),
            sum(self.found.values()),
            sum(self.missed.values()),
            sum(self.prompts.values()),
        )

    def per_class(self) -> list[dict]:
        rows = []
        for class_id, name in self.classes.items():
            total_instances = self.found[class_id] + self.missed[class_id]
            total_prompts = self.prompts[class_id]
            if total_instances == 0 and total_prompts == 0:
                continue
            row = self._metrics(
                self.tp[class_id],
                self.fp[class_id],
                self.found[class_id],
                self.missed[class_id],
                total_prompts,
            )
            row["category"] = name
            rows.append(row)
        rows.sort(key=lambda item: item["total_instances"], reverse=True)
        return rows

    def to_json(self) -> dict:
        return {
            "tp": dict(self.tp),
            "fp": dict(self.fp),
            "found": dict(self.found),
            "missed": dict(self.missed),
            "prompts": dict(self.prompts),
        }

    @classmethod
    def from_json(cls, classes: dict[int, str], payload: dict | None):
        ev = cls(classes)
        if not payload:
            return ev
        for name in ("tp", "fp", "found", "missed", "prompts"):
            store = getattr(ev, name)
            for key, value in payload.get(name, {}).items():
                store[int(key)] = int(value)
        return ev


def _prepare_gt_instances(lvis_api, anns: list[dict], valid_cats: set[int]) -> dict[int, list[np.ndarray]]:
    gt_instances = {}
    for class_id in valid_cats:
        class_anns = [ann for ann in anns if ann["category_id"] == class_id]
        masks = []
        for ann in class_anns:
            mask = lvis_api.ann_to_mask(ann).astype(np.uint8)
            if mask.sum() > 0:
                masks.append(mask)
        gt_instances[class_id] = masks
    return gt_instances


def _point_payload(cache_payload: dict | None, method: str, class_id: int, hybrid_threshold: float):
    if cache_payload is None:
        return None
    if method == "relative_similarity":
        return _deserialize_point_tuple(cache_payload.get("filtered", {}).get(str(class_id)))
    prompts = cache_payload.get("text_filtered", {}).get(str(class_id))
    if prompts is None:
        return None
    accepted = [p for p in prompts if float(p.get("score", 0.0)) >= hybrid_threshold]
    coords = np.asarray([[p["x"], p["y"]] for p in accepted], dtype=np.float32).reshape(-1, 2)
    labels = np.ones(len(coords), dtype=np.int32)
    scores = np.asarray([p["score"] for p in accepted], dtype=np.float32)
    return coords, labels, scores


class BankCache:
    def __init__(self, feature_bank_dir: str, device: str, cpu_size: int = 4, gpu_size: int = 1):
        self.feature_bank_dir = feature_bank_dir
        self.device = device
        self.cpu_size = max(0, cpu_size)
        self.gpu_size = max(0, gpu_size)
        self.cpu_cache: OrderedDict[str, torch.Tensor | None] = OrderedDict()
        self.gpu_cache: OrderedDict[str, torch.Tensor | None] = OrderedDict()

    def _load_cpu(self, cat_name: str, max_references: int | None):
        cat_dir = os.path.join(self.feature_bank_dir, cat_name)
        if not os.path.isdir(cat_dir):
            return None
        files = sorted(f for f in os.listdir(cat_dir) if f.endswith(".pt"))
        if not files:
            return None
        all_feats = []
        total = 0
        for fname in files:
            feats = torch.load(os.path.join(cat_dir, fname), map_location="cpu", weights_only=True).float()
            if max_references is not None:
                remaining = max_references - total
                if remaining <= 0:
                    break
                if feats.shape[0] > remaining:
                    feats = feats[:remaining]
            all_feats.append(feats)
            total += feats.shape[0]
        if not all_feats:
            return None
        return torch.cat(all_feats, dim=0)

    def get(self, cat_name: str, max_references: int | None):
        if cat_name in self.gpu_cache:
            tensor = self.gpu_cache.pop(cat_name)
            self.gpu_cache[cat_name] = tensor
            return tensor
        if cat_name in self.cpu_cache:
            cpu_tensor = self.cpu_cache.pop(cat_name)
            self.cpu_cache[cat_name] = cpu_tensor
        else:
            cpu_tensor = self._load_cpu(cat_name, max_references)
            self.cpu_cache[cat_name] = cpu_tensor
            while len(self.cpu_cache) > self.cpu_size > 0:
                self.cpu_cache.popitem(last=False)
        if cpu_tensor is None:
            return None
        gpu_tensor = F.normalize(cpu_tensor.to(self.device).float(), dim=-1)
        self.gpu_cache[cat_name] = gpu_tensor
        while len(self.gpu_cache) > self.gpu_size > 0:
            _, old = self.gpu_cache.popitem(last=False)
            del old
            if self.device == "cuda":
                torch.cuda.empty_cache()
        return gpu_tensor


def _seg_metrics_template():
    return {"TP": 0, "FP": 0, "TN": 0, "FN": 0}


def _seg_compute_metrics(m):
    tp, fp, tn, fn = m["TP"], m["FP"], m["TN"], m["FN"]
    total = tp + fp + tn + fn
    precision = tp / (tp + fp) if (tp + fp) > 0 else 0.0
    recall = tp / (tp + fn) if (tp + fn) > 0 else 0.0
    f1 = 2 * precision * recall / (precision + recall) if (precision + recall) > 0 else 0.0
    accuracy = (tp + tn) / total if total > 0 else 0.0
    return {
        "Precision": precision,
        "Recall": recall,
        "F1": f1,
        "Accuracy": accuracy,
        "TP": tp,
        "FP": fp,
        "TN": tn,
        "FN": fn,
    }


def _absolute_eval(args) -> dict:
    lvis_api = LVIS(args.annotation_file)
    cats = lvis_api.load_cats(lvis_api.get_cat_ids())
    classes = {cat["id"]: cat["name"] for cat in cats}
    img_ids = lvis_api.get_img_ids()
    if args.max_images is not None:
        img_ids = img_ids[:args.max_images]
    records = _build_records(lvis_api, img_ids, args.image_dir)
    device = "cuda" if torch.cuda.is_available() else "cpu"
    dino_extractor = DINOv3FeatureExtractor(annotation_file=args.annotation_file)
    bank_cache = BankCache(args.feature_bank_dir, device=device, cpu_size=4, gpu_size=1)
    threshold = float(args.absolute_threshold)
    global_metrics = _seg_metrics_template()
    per_category = defaultdict(_seg_metrics_template)
    missing_bank_by_category = defaultdict(int)

    dataset = LVISPrefetchDataset(records)
    loader = DataLoader(
        dataset,
        batch_size=1,
        num_workers=DATALOADER_NUM_WORKERS,
        collate_fn=collate_records,
        prefetch_factor=PREFETCH_QUEUE_SIZE if DATALOADER_NUM_WORKERS > 0 else None,
        pin_memory=torch.cuda.is_available(),
        persistent_workers=DATALOADER_NUM_WORKERS > 0,
    )

    for batch in tqdm(loader, total=len(records), desc="Evaluating absolute_similarity"):
        rec = batch[0]
        if rec.pil_image is None:
            continue
        query_features = dino_extractor.extract(rec.pil_image, cache_key=rec.file_name)
        grid_h, grid_w, dim = query_features.shape
        query_flat = F.normalize(query_features.reshape(-1, dim).float(), dim=-1).to(device)
        original_size = (rec.img_info["height"], rec.img_info["width"])
        gt_instances = _prepare_gt_instances(lvis_api, rec.anns, rec.valid_cats)

        for class_id in rec.valid_cats:
            gt_masks = gt_instances[class_id]
            if not gt_masks:
                continue
            class_name = classes[class_id]
            bank = bank_cache.get(class_name, args.max_references)
            if bank is None:
                missing_bank_by_category[class_name] += 1
                continue
            best_sim, _ = (query_flat @ bank.T).max(dim=1)
            positive = (best_sim > threshold).reshape(grid_h, grid_w).detach().cpu()

            union_mask = np.zeros(original_size, dtype=np.uint8)
            for mask in gt_masks:
                union_mask = np.logical_or(union_mask, mask).astype(np.uint8)

            image_size = IMAGE_SIZE
            patch_h = image_size // grid_h
            patch_w = image_size // grid_w
            pred_up = positive.repeat_interleave(patch_h, 0).repeat_interleave(patch_w, 1).numpy().astype(np.uint8)
            pred_mask = align_mask(pred_up, original_size).astype(bool)
            gt_bool = union_mask.astype(bool)

            tp = int(np.logical_and(pred_mask, gt_bool).sum())
            fp = int(np.logical_and(pred_mask, ~gt_bool).sum())
            fn = int(np.logical_and(~pred_mask, gt_bool).sum())
            tn = int(np.logical_and(~pred_mask, ~gt_bool).sum())
            for store in (global_metrics, per_category[class_name]):
                store["TP"] += tp
                store["FP"] += fp
                store["FN"] += fn
                store["TN"] += tn
        del query_features, query_flat
        gc.collect()
        if device == "cuda":
            torch.cuda.empty_cache()

    return {
        "config": {"threshold": threshold},
        "global": _seg_compute_metrics(global_metrics),
        "per_category": [
            {"category": name, **_seg_compute_metrics(m)}
            for name, m in sorted(per_category.items())
        ],
        "missing_bank_by_category": dict(sorted(missing_bank_by_category.items())),
    }


def _point_eval(method: str, args) -> dict:
    lvis_api = LVIS(args.annotation_file)
    cats = lvis_api.load_cats(lvis_api.get_cat_ids())
    classes = {cat["id"]: cat["name"] for cat in cats}
    checkpoint_dir = os.path.join(args.dataset_root, "_github_non_sam_eval")
    checkpoint_path = os.path.join(checkpoint_dir, f"{method}.json")
    state = load_json(checkpoint_path, {"processed_image_ids": [], "evaluator": None, "missing_bank_by_category": {}}) if args.resume else {"processed_image_ids": [], "evaluator": None, "missing_bank_by_category": {}}
    processed = set(state.get("processed_image_ids", []))
    evaluator = PromptPointEvaluator.from_json(classes, state.get("evaluator"))
    missing_bank_by_category = defaultdict(int, state.get("missing_bank_by_category", {}))

    img_ids = lvis_api.get_img_ids()
    if args.max_images is not None:
        img_ids = img_ids[:args.max_images]
    records = _build_records(lvis_api, img_ids, args.image_dir)
    cache_dir = _prompt_cache_dir(args, method)
    if not os.path.isdir(cache_dir):
        raise FileNotFoundError(f"Prompt cache directory not found for {method}: {cache_dir}")
    dataset = LVISPrefetchDataset(records)
    loader = DataLoader(
        dataset,
        batch_size=1,
        num_workers=DATALOADER_NUM_WORKERS,
        collate_fn=collate_records,
        prefetch_factor=PREFETCH_QUEUE_SIZE if DATALOADER_NUM_WORKERS > 0 else None,
        pin_memory=torch.cuda.is_available(),
        persistent_workers=DATALOADER_NUM_WORKERS > 0,
    )
    for batch in tqdm(loader, total=len(records), desc=f"Evaluating {method}"):
        rec = batch[0]
        if rec.img_id in processed:
            continue
        gt_instances = _prepare_gt_instances(lvis_api, rec.anns, rec.valid_cats)
        cache_payload = load_prompt_cache(cache_dir, rec.file_name)
        for class_id in rec.valid_cats:
            class_name = classes[class_id]
            masks = gt_instances[class_id]
            if not masks:
                continue
            payload = _point_payload(cache_payload, method, class_id, args.hybrid_threshold)
            if payload is None:
                missing_bank_by_category[class_name] += 1
                continue
            coords, _, _ = payload
            evaluator.update(class_id, coords, masks)
        processed.add(rec.img_id)
        save_json_atomic(
            checkpoint_path,
            {
                "processed_image_ids": sorted(processed),
                "evaluator": evaluator.to_json(),
                "missing_bank_by_category": dict(sorted(missing_bank_by_category.items())),
            },
        )
    if os.path.exists(checkpoint_path):
        os.remove(checkpoint_path)
    return {
        "global": evaluator.global_metrics(),
        "per_category": evaluator.per_class(),
        "missing_bank_by_category": dict(sorted(missing_bank_by_category.items())),
    }


def run(args):
    _resolve_paths(args)
    methods = METHODS if args.method == COMPARE_ALL else (args.method,)
    results = {}
    started = time.time()
    for method in methods:
        if method == "absolute_similarity":
            results[method] = _absolute_eval(args)
        elif method in ("relative_similarity", "hybrid"):
            results[method] = _point_eval(method, args)
        else:
            raise ValueError(f"Unsupported method: {method}")
    return {
        "config": {
            "dataset_root": args.dataset_root,
            "annotation_file": args.annotation_file,
            "image_dir": args.image_dir,
            "feature_bank_dir": args.feature_bank_dir,
            "max_images": args.max_images,
            "max_references": args.max_references,
            "absolute_threshold": args.absolute_threshold,
            "relative_threshold": args.relative_threshold,
            "hybrid_threshold": args.hybrid_threshold,
            "min_peak_distance": args.min_peak_distance,
            "min_component_size": args.min_component_size,
            "elapsed_seconds": time.time() - started,
        },
        "methods": results,
    }
