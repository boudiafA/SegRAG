"""
Native SegRAG implementation for non-SAM feature-matching evaluation.

Method behavior:
- absolute_similarity: dense patch-level foreground/background evaluation
- relative_similarity: point-level precision / instance recall from cached peaks
- hybrid: point-level precision / instance recall from cached dual-threshold prompts

The point-based methods consume the shared prompt caches generated by
`segrag.stages.cache_prompts`.
"""

